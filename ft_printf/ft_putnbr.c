#include "ft_printf.h"

void	ft_putnbr(long long nb, size_t *a)
{
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		(*a) += 11;
	}
	else
	{
		if (nb < 0)
		{
			write(1, "-", 1);
			nb = nb * (-1);
			(*a) += 1;
		}
		if (nb >= 10)
		{
			ft_putnbr(nb / 10, a);
			ft_putnbr(nb % 10, a);
		}
		if (nb >= 0 && nb < 10)
		{
			ft_putchar(nb + '0', a);
		}
	}
}
