#include "ft_printf.h"

void	ft_putstr(const char *s, size_t *a)
{
	int	i;

	i = 0;
	if (!s)
	{
		write(1, "(null)", 6);
		(*a) = (*a) + 6;
		return ;
	}
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
		(*a)++;
	}
}
